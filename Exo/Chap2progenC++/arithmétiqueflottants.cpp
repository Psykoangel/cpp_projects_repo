// Teste la virgule flottante a l'aide des operateurs +, -, * et /

#include <iostream>

using namespace std;

int main() {

  double x = 54.0;
  double y = 20.0;

  cout << "x = " << x << " et y = " << y << endl;
  cout << "x+y = " << x + y << endl; // 54.0 + 20.0 = 74.0
  cout << "x-y = " << x - y << endl; // 54.0 - 20.0 = 34.0
  cout << "x*y = " << x * y << endl; // 54.0 * 20.0 = 1080.0
  cout << "x/y = " << x / y << endl; // 54.0 / 20.0 = 2.7

  return 0;
}
