
// Teste les operateurs +, -, *, / et % :
#include <iostream>

using namespace std;

int main() {

  int m = 54;
  int n = 20;

  cout << "m = " << m << " et n = " << n << endl;
  cout << "m+n = " << m + n << endl; // 54 + 20 = 74
  cout << "m-n = " << m - n << endl; // 54 - 20 = 34
  cout << "m*n = " << m * n << endl; // 54 * 20 = 1080
  cout << "m/n = " << m / n << endl; // 54 / 20 = 2
  cout << "m%n = " << m % n << endl; // 54 % 20 = 14

  return 0;
}
