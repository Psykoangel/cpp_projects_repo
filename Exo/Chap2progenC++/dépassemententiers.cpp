#include <iostream>

using namespace std;

int main() {

  int n = 1000;
  cout << "n = " << n << endl;

  n *= 1000; // Multiplie n par 1000
  cout << "n = " << n << endl;

  n *= 1000; // Multiplie n par 1000
  cout << "n = " << n << endl;

  n *= 1000; // Multiplie n par 1000
  cout << "n = " << n << endl;

  return 0;
}
