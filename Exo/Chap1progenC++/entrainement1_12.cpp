#include <iostream>

using namespace std;

int main() {
  // Imprime les valeurs des variables non initialisees
  bool b; // non initialisee
  cout << "b = " << b << endl;
  char c; // non initialisee
  cout << "c = [" << c << "]" << endl;
  int m; // non initialisee
  cout << "m = " << m << endl;
  int n; // non initialisee
  cout << "n = " << n << endl;
  long nn; // non initialisee
  cout << "nn = " << nn << endl;
  float x; // non initialisee
  cout << "x = " << x << endl;
  double y; // non initialisee
  cout << "y = " << y << endl;
  return 0;
}
